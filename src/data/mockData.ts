function makeSessions(baseScore: number, trend: 'up' | 'down' | 'stable'): { date: string; score: number; curiositySpike: boolean; confusionFlag: boolean; topic: string }[] {
  const days = 10
  return Array.from({ length: days }, (_, i) => {
    const delta = trend === 'up' ? i * 2 : trend === 'down' ? -i * 3 : (Math.random() - 0.5) * 8
    const score = Math.min(100, Math.max(10, baseScore + delta + (Math.random() - 0.5) * 10))
    return {
      date: new Date(Date.now() - (days - i) * 86400000).toLocaleDateString('en-US', { month: 'short', day: 'numeric' }),
      score: Math.round(score),
      curiositySpike: score > 75 && Math.random() > 0.6,
      confusionFlag: score < 45 && Math.random() > 0.5,
      topic: topics[Math.floor(Math.random() * topics.length)],
    }
  })
}

